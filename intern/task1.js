let admin; // Пишем пустую переменную, в которое позже мы передадим значение
let name = "Дима"; // Создаем вторую переменную, в которое вносим любое значение, в моем случае это имя Дима

admin = name; // Присваиваем пустой переменной admin значение из переменной name, теперь значения в обеих переменных - одинаковые

console.log(admin); // Выводим на экран 
